u_long
scan_anim(u_long type)
{
	/* CTG 3: ANIMATION */

	GsUNIT_AnimFuncp	func = NULL;

	assert((type >> 24) == 0x03);

	switch (type) {
	case 0x03800000:	func = GsU_03000000;	break;
	default:
		printf("unsupported ANIMATION primitive 0x%08x.\n", type);
	}

	return (u_long)func;
}

